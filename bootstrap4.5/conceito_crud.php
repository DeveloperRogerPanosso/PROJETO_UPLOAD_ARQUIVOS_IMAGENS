					<big>
						<span>
							O conceito de CRUD refere-se há um sistema dinâmico, cujo objetivo<br/>
							é obter dados consistentes e persistentes diante de bancos de dados<br/>
							relacionais como o MySQL por exemplo obtendo processos de consultas<br/>
							SQL para manipulações de dados.
						</span>
						<BR/>
						<span>
							Um CRUD mais precisamente é um sistema que trabalha com manipulações de<br/>
							dados de acordo com as categorias da classe DML(data manipulation language)<br/>
							da linguagem SQL. Onde pode se trabalhar com inserção de dados(INSERT)<br/>
							seleção de dados para visualizações em formas estruturadas(SELECT),<br/>
							atualização de dados(UPDATE), e remoção de dados(DELETE).
						</span>
						<BR/>
						<span>
							Portanto seu nome descreve suas principais funcionalidades<br/>
							<pre>
								C - CREATE(INSERT)
								R - READ(SELECT)
								U - UPDATE(UPDATE)
								D - DELETE(DELETE)
							</pre>
						</span>
					</big>
